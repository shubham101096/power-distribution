public class HubImpact {

    String hubID;
    float impact;

    public HubImpact(String hubID, float impact) {
        this.hubID = hubID;
        this.impact = impact;
    }

    public String getHubID() {
        return hubID;
    }

    public void setHubID(String hubID) {
        this.hubID = hubID;
    }

    public float getImpact() {
        return impact;
    }

    public void setImpact(float impact) {
        this.impact = impact;
    }

}
